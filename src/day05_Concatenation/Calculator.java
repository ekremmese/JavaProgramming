package day05_Concatenation;

public class Calculator {

    public static void main(String[] args) {

        int firstn = 100;
        int secondn = 50;

        int sum = firstn+secondn;
        int multiply = firstn*secondn;
        int substraction = firstn-secondn;

        System.out.println(firstn + " + " + secondn + " = " + sum);
        System.out.println(firstn + " * " + secondn + " = " + multiply);
        System.out.println(firstn + " - " + secondn + " = " + substraction);
    }
}
