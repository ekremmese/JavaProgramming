package day05_Concatenation;

public class CarInfo {

    public static void main(String[] args) {

        String year = "2018",
                make = "Mercedes",
                model = "Vito",
                color = "Blue";
        int miles = 100000;
        double price = 25000;
        System.out.println(year + " " + make + ", " + model + ", " + miles + " miles, " + color + " " + "$" + price);
    }


}
